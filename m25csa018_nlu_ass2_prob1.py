            plt.title('Most Frequent Words in IIT Jodhpur Corpus', fontsize=16, pad=20)
            plt.tight_layout()
            plt.savefig(save_path, dpi=300, bbox_inches='tight')
            plt.show()

            print(f"\nWord cloud saved as: {save_path}")

