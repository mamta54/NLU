class EmbeddingVisualizer:
    """
    Visualize word embeddings using PCA and t-SNE
    """
    def __init__(self, models, words_to_visualize):
        self.models = models
        self.words = words_to_visualize

    def reduce_dimensions(self, model, method='pca'):
        """
        Reduce dimensionality of word embeddings
        """
        # Get embeddings for selected words
        embeddings = []
        valid_words = []

        for word in self.words:
            try:
                embeddings.append(model.wv[word])
                valid_words.append(word)
            except KeyError:
                print(f"  Word '{word}' not in vocabulary, skipping...")

        if len(embeddings) < 2:
            print("  Not enough valid words for visualization")
            return None, valid_words

        embeddings = np.array(embeddings)

        if method == 'pca':
            reducer = PCA(n_components=2, random_state=42)
            reduced = reducer.fit_transform(embeddings)
        else:  # t-SNE
            # Adjust perplexity based on number of points
            perplexity = min(30, len(embeddings) - 1) if len(embeddings) > 1 else 1
            reducer = TSNE(n_components=2, random_state=42, perplexity=perplexity)
            reduced = reducer.fit_transform(embeddings)

        return reduced, valid_words

    def visualize_clusters(self, save_path='embeddings_visualization.png'):
        """
        Visualize word embeddings clusters
        """
        n_models = len(self.models)
        fig, axes = plt.subplots(n_models, 2, figsize=(16, 5 * n_models))

        # Handle single model case
        if n_models == 1:
            axes = axes.reshape(1, -1)

        for idx, (model_name, model) in enumerate(self.models.items()):
            print(f"\nVisualizing {model_name}...")

            # PCA
            pca_result = self.reduce_dimensions(model, method='pca')
            if pca_result[0] is not None:
                pca_coords, pca_words = pca_result

                axes[idx, 0].scatter(pca_coords[:, 0], pca_coords[:, 1], alpha=0.6, s=100, c='steelblue')
                for i, word in enumerate(pca_words):
                    axes[idx, 0].annotate(word, (pca_coords[i, 0], pca_coords[i, 1]),
                                        fontsize=9, alpha=0.8, ha='center', va='bottom')
                axes[idx, 0].set_title(f'{model_name} - PCA Projection', fontsize=12, pad=15)
                axes[idx, 0].grid(True, alpha=0.3)

            # t-SNE
            tsne_result = self.reduce_dimensions(model, method='tsne')
            if tsne_result[0] is not None:
                tsne_coords, tsne_words = tsne_result

                axes[idx, 1].scatter(tsne_coords[:, 0], tsne_coords[:, 1], alpha=0.6, s=100, c='coral')
                for i, word in enumerate(tsne_words):
                    axes[idx, 1].annotate(word, (tsne_coords[i, 0], tsne_coords[i, 1]),
                                        fontsize=9, alpha=0.8, ha='center', va='bottom')
                axes[idx, 1].set_title(f'{model_name} - t-SNE Projection', fontsize=12, pad=15)
                axes[idx, 1].grid(True, alpha=0.3)

        plt.suptitle('Word Embeddings Visualization: CBOW vs Skip-gram',
                    fontsize=16, y=1.02)
        plt.tight_layout()
        plt.savefig(save_path, dpi=300, bbox_inches='tight')
        plt.show()

        print(f"\nVisualization saved as: {save_path}")

