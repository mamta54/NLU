            analogy2 = self.analogies(model, 'research', 'professor', 'study')
            if analogy2:
                for word, score in analogy2:
                    print(f"    - {word}: {score:.4f}")
            else:
                print("    No result found")

            # Experiment 3: bachelor : undergraduate :: master : ?
            print("\n  Analogy 3: bachelor : undergraduate :: master : ?")
            analogy3 = self.analogies(model, 'bachelor', 'undergraduate', 'master')
