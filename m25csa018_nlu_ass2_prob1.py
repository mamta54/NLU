class SemanticAnalyzer:
    """
    Perform semantic analysis using trained word embeddings
    """
    def __init__(self, models):
        self.models = models

    def find_nearest_neighbors(self, model, word, top_n=5):
        """
        Find top N nearest neighbors for a given word
        """
        try:
            neighbors = model.wv.most_similar(word, topn=top_n)
            return neighbors
        except KeyError:
            print(f"  Word '{word}' not in vocabulary")
            return []

    def analogies(self, model, word1, word2, word3, top_n=3):
        """
        Perform analogy: word1 : word2 :: word3 : ?
        """
        try:
            result = model.wv.most_similar(positive=[word2, word3], negative=[word1], topn=top_n)
            return result
        except KeyError as e:
            print(f"  Word not found: {e}")
            return []

    def analyze_semantics(self, target_words):
        """
        Analyze semantic relationships for target words
        """
        print("\n" + "="*50)
        print("SEMANTIC ANALYSIS")
        print("="*50)

        for model_name, model in self.models.items():
            print(f"\n{'='*50}")
            print(f"Model: {model_name}")
            print('='*50)

            # Task 3.1: Nearest neighbors
            print("\n1. NEAREST NEIGHBORS:")
            for word in target_words:
                neighbors = self.find_nearest_neighbors(model, word)
                if neighbors:
                    print(f"\n  '{word}':")
                    for neighbor, score in neighbors:
                        print(f"    - {neighbor}: {score:.4f}")
                else:
                    print(f"\n  '{word}': No similar words found")

            # Task 3.2: Analogy experiments
            print("\n2. ANALOGY EXPERIMENTS:")

            # Experiment 1: UG : BTech :: PG : ?
            print("\n  Analogy 1: UG : BTech :: PG : ?")
            analogy1 = self.analogies(model, 'ug', 'btech', 'pg')
            if analogy1:
                for word, score in analogy1:
                    print(f"    - {word}: {score:.4f}")
