class NameGenerator:
    """Train and evaluate name generation models"""
    def __init__(self, model, dataset, learning_rate=0.001, device='cpu'):
        self.model = model.to(device)
        self.dataset = dataset
        self.device = device
        self.criterion = nn.CrossEntropyLoss()
        self.optimizer = optim.Adam(model.parameters(), lr=learning_rate)

    def train(self, epochs=15, batch_size=32):
        """Train the model"""
        dataloader = DataLoader(self.dataset, batch_size=batch_size, shuffle=True)
        losses = []

        print(f"\n{'='*50}")
        print(f"Training {self.model.__class__.__name__}")
        print(f"{'='*50}")
        total_params = sum(p.numel() for p in self.model.parameters())
        print(f"Trainable parameters: {total_params:,}")
        print(f"Hidden size: 128, Layers: 2, Learning rate: 0.001")

        for epoch in range(epochs):
            total_loss = 0
            for batch_idx, (data, target) in enumerate(dataloader):
                data, target = data.to(self.device), target.to(self.device)
                hidden = self.model.init_hidden(data.size(0))

                # Move hidden to device
                if isinstance(hidden, tuple):
                    hidden = (hidden[0].to(self.device), hidden[1].to(self.device))
                else:
                    hidden = hidden.to(self.device)

                self.optimizer.zero_grad()
                output, hidden = self.model(data, hidden)
                loss = self.criterion(output, target)
                loss.backward()

                # Gradient clipping
                torch.nn.utils.clip_grad_norm_(self.model.parameters(), 1.0)

                self.optimizer.step()
                total_loss += loss.item()

            avg_loss = total_loss / len(dataloader)
            losses.append(avg_loss)

            if (epoch + 1) % 10 == 0:
                print(f"  Epoch {epoch+1}/{epochs}, Loss: {avg_loss:.4f}")

        return losses

    def generate_name(self, max_length=25, temperature=0.5):
        """Generate a name with surname - FIXED"""
        self.model.eval()

        # Start with a random character
        start_char = random.choice(['a', 'r', 's', 'm', 'k', 'p', 'v', 'n', 'd', 'g'])

        # Use list for characters
        name_chars = [start_char]
        hidden = self.model.init_hidden(1)

        if isinstance(hidden, tuple):
            hidden = (hidden[0].to(self.device), hidden[1].to(self.device))
        else:
            hidden = hidden.to(self.device)

        space_added = False

        for step in range(max_length):
            # Prepare input sequence
            seq = []
            for ch in name_chars[-self.dataset.seq_length:]:
                if ch in self.dataset.char_to_idx:
                    seq.append(self.dataset.char_to_idx[ch])
                else:
                    seq.append(self.dataset.char_to_idx['<START>'])

            # Pad if needed
            while len(seq) < self.dataset.seq_length:
                seq.insert(0, self.dataset.char_to_idx['<START>'])

            input_tensor = torch.tensor([seq], dtype=torch.long).to(self.device)

            # Generate next character
            with torch.no_grad():
                output, hidden = self.model(input_tensor, hidden)
                probs = torch.softmax(output / temperature, dim=1)
                next_idx = torch.multinomial(probs, 1).item()
                next_char = self.dataset.idx_to_char[next_idx]

            # Stop if end token
            if next_char == '<END>':
                break

            # Add space after first name (around 4-7 characters)
            if not space_added and len(name_chars) >= random.randint(4, 7):
              name_chars.append(' ')
              space_added = True
              continue

            name_chars.append(next_char)

            # Stop after surname is complete (check if we have space and enough characters)
            # Remove or modify this line:
            if space_added and len(name_chars) > len(''.join(name_chars).split()[0]) + 5:
                break
            if len(name_chars) > 20:  # Allow longer names
                break

        # Convert list to string
        result = ''.join(name_chars).strip()

        # Ensure we have both first name and surname
        if ' ' not in result:
            result = result.capitalize()

        return result

