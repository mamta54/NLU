class CharacterDataset(Dataset):
    """
    Dataset for character-level sequence modeling
    """
    def __init__(self, names, seq_length=20):
        # Collect all characters
        chars = set()
        for name in names:
            chars.update(name.lower())

        # Add special tokens
        self.chars = ['<START>', '<END>'] + sorted(list(chars))
        self.char_to_idx = {ch: i for i, ch in enumerate(self.chars)}
        self.idx_to_char = {i: ch for i, ch in enumerate(self.chars)}
        self.vocab_size = len(self.chars)
        self.seq_length = seq_length

        # Prepare sequences
        self.sequences = []
        self.targets = []

        for name in names:
            # Convert to lowercase
            name = name.lower()
            # Add start and end tokens
            seq = ['<START>'] + list(name) + ['<END>']

            # Create sequences
            for i in range(len(seq) - 1):
                # Get previous seq_length characters
                start_idx = max(0, i - seq_length + 1)
                input_seq = seq[start_idx:i+1]

                # Pad if needed
                while len(input_seq) < seq_length:
                    input_seq.insert(0, '<START>')

                target_char = seq[i+1]

                # Convert to indices
                input_indices = [self.char_to_idx[ch] for ch in input_seq]
                target_idx = self.char_to_idx[target_char]

                self.sequences.append(input_indices)
                self.targets.append(target_idx)

        print(f"\n{'='*50}")
        print(f"DATA PREPARATION")
        print(f"{'='*50}")
        print(f"✓ Total sequences created: {len(self.sequences)}")
        print(f"✓ Vocabulary size: {self.vocab_size}")
        print(f"✓ Sequence length: {seq_length}")
        print(f"✓ Characters: {', '.join(self.chars[:15])}...")

        # Verify we have data
        if len(self.sequences) == 0:
            raise ValueError("No sequences created! Check your data.")

    def __len__(self):
        return len(self.sequences)

    def __getitem__(self, idx):
        return (torch.tensor(self.sequences[idx], dtype=torch.long),
                torch.tensor(self.targets[idx], dtype=torch.long))

