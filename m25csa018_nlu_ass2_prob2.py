def main():
    # Setup device
    device = torch.device('cuda' if torch.cuda.is_available() else 'cpu')
    print(f"\nUsing device: {device}")

    # TASK 0: Generate dataset
    dataset_gen = IndianNameDataset()
    names = dataset_gen.generate_names(1000)

    # Create character dataset
    char_dataset = CharacterDataset(names, seq_length=20)

    # Verify dataset has sequences
    if len(char_dataset) == 0:
        print("\nERROR: No sequences generated! Exiting.")
        return

    # TASK 1: Initialize models
    vocab_size = char_dataset.vocab_size

    models = {
        'Vanilla RNN': VanillaRNN(vocab_size, hidden_size=128, num_layers=2),
        'Bidirectional LSTM': BidirectionalLSTM(vocab_size, hidden_size=128, num_layers=2),
        'RNN with Attention': AttentionRNN(vocab_size, hidden_size=128, num_layers=2)
    }

    # Train models
    trained_models = {}
    training_losses = {}

    for name, model in models.items():
        trainer = NameGenerator(model, char_dataset, learning_rate=0.001, device=device)
        losses = trainer.train(epochs=35, batch_size=64)
        trained_models[name] = trainer
        training_losses[name] = losses

    # Plot training curves
    plt.figure(figsize=(12, 6))
    for name, losses in training_losses.items():
        plt.plot(losses, label=name, linewidth=2)
