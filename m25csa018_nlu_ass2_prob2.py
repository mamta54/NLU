def evaluate_models(models_dict, dataset, training_names, num_samples=150):
    """Evaluate all models"""
    print(f"\n{'='*50}")
    print(f"TASK 2: QUANTITATIVE EVALUATION")
    print(f"{'='*50}")

    results = {}
    training_set = set([name.lower() for name in training_names])

    for name, trainer in models_dict.items():
        print(f"\n{'='*50}")
        print(f"Evaluating {name}")
        print(f"{'='*50}")

        generated_names = []
        for i in range(num_samples):
            gen_name = trainer.generate_name(max_length=25)
            generated_names.append(gen_name.lower())

        unique_names = set(generated_names)
        novel_names = [n for n in unique_names if n not in training_set]

        novelty_rate = len(novel_names) / len(unique_names) if unique_names else 0
        diversity = len(unique_names) / len(generated_names) if generated_names else 0

        results[name] = {
            'generated': generated_names[:30],
            'unique_count': len(unique_names),
            'novelty_rate': novelty_rate,
            'diversity': diversity,
            'total_generated': len(generated_names)
        }

        print(f"\nMetrics:")
        print(f"  • Total generated: {len(generated_names)}")
        print(f"  • Unique names: {len(unique_names)}")
        print(f"  • Novelty rate: {novelty_rate:.2%}")
        print(f"  • Diversity: {diversity:.2%}")

        print(f"\nSample generated names (first 15):")
        for i, name in enumerate(generated_names[:15], 1):
            print(f"  {i:2d}. {name.title()}")

    return results

